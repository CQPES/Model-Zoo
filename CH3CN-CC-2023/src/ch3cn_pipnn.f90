module nnparam
    implicit none
    real(kind=8), parameter :: alpha = 1.0d0, PI = 3.1415926d0, radian = PI/180.0d0
    !> NOTE: nbasis, ndim, natom
    integer, parameter :: nbasis = 1827, ndim = 15, natom = 6
    !> NOTE: vpesmin
    real(kind=8), parameter :: vpesmin = -132.578846545877d0
    integer ninput, noutput, nhid, nlayer, ifunc, nwe, nodemax
    integer nscale
    real(kind=8), allocatable :: pesnn(:), pes(:), delta1(:)
    integer, allocatable :: nodes(:)
    real(kind=8), allocatable :: weighta(:, :, :), biasa(:, :)
    real(kind=8), allocatable :: pdela(:), pavga(:)

end module nnparam

subroutine ch3cn_pipnn(ct, vpes)
    use nnparam
    implicit none
    integer i, j, k, lpes
    real(kind=8) rb(ndim), xbond(ndim), basis(nbasis), tmp1, txinput(nbasis - 1)
    real(kind=8) ct(3, natom), xvec(3, ndim), cx(3, natom), cy(3, natom)
    real(kind=8) vpes, vpesa, vpesb, vpesc, vpesH, vnn

    basis(:) = 0.0d0

    cx = ct

    k = 0
    do i = 1, natom - 1
        do j = i + 1, natom
            k = k + 1
            ! write(*,*)k,i,j
            xvec(:, k) = cx(:, i) - cx(:, j)
        end do
    end do

    if (k /= ndim) stop 'error in bond dimension'

    do i = 1, ndim
        rb(i) = dsqrt(dot_product(xvec(:, i), xvec(:, i))) ! 点积再开根号, 相当于 (x**2 + y**2 + z**2)**0.5
    end do

    xbond(:) = dexp(-rb(:)/alpha)

    ! 生成 pip 多项式
    call bemsav(xbond, basis)

    do j = 1, nbasis - 1
        txinput(j) = basis(j + 1)
    end do
    ! print *, txinput(1), txinput(nbasis - 1)
    ! print *, basis(1), basis(2), basis(nbasis)
    ! write(987,'(2000E16.8)')txinput(:)

    call getpota(txinput, vpes)
    ! vnn=vpes
    ! vpes=vnn
    
    return
end subroutine ch3cn_pipnn

