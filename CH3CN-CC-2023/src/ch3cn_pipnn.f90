subroutine bemsav(x,p)
    implicit none
    real*8,dimension(1:15),intent(in)::x
    real*8,dimension(0:1826),intent(out)::p
    ! ::::::::::::::::::::
    real*8,dimension(0:1072)::m

    call evmono(x,m)
    call evpoly(m,p)

    return
end subroutine bemsav

